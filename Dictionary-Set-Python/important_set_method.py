set1 = {1, 2, 3, 4}
set2 = {1, 2, "Hello", "Learn Python with me"}

# union of sets => combines both sets value and returns new set
unionSet = set1.union(set2)
print("Union of set1 and set2 =>", unionSet)


# intersection of sets
intersectionSet = set1.intersection(set2)
print("Intersection of set1 and set2 is =>", intersectionSet)
