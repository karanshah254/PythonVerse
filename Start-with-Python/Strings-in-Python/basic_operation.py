# 1. concatenation: add two strings
string1 = "hello"
string2 = "world"
print(string1 + " " + string2)  # print hello world

# 2. Length of string
my_str = "Hello, World!"
print("Length is", len(my_str))

# 3. Indexing: index starts from 0
print("Character at index 1 is", my_str[1])

# 4. Slicing: Accessing parts of a string
print(my_str[1:4])  # gives "ell"
# Negetive index
print(my_str[-5:-2])